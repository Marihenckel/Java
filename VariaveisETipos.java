public class VariaveisETipos {

    public static void main(String[] args) {
        // Declaração de variáveis de diferentes tipos
        int idade = 25; // Tipo inteiro
        double altura = 1.75; // Tipo ponto flutuante
        char genero = 'M'; // Tipo caractere
        boolean estudante = true; // Tipo booleano
        String nome = "João"; // Tipo String

        // Exibição dos valores das variáveis no console
        System.out.println("Nome: " + nome);
        System.out.println("Idade: " + idade);
        System.out.println("Altura: " + altura);
        System.out.println("Gênero: " + genero);
        System.out.println("Estudante: " + estudante);
    }
}